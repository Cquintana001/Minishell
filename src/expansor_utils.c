int	check_next_char(char c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z') || (c >= 'a'
			&& c <= 'z') || c == '_' || c == '?')
		return (0);
	return (1);
}

int	len(char *str)
{
	int	x;

	x = 0;
	str++;
	if (*str == '?' || *str == '_')
		return (1);
	while (*str && ((*str >= '0' && *str <= '9') || (*str >= 'A' && *str <= 'Z')
			|| (*str >= 'a' && *str <= 'z') || *str == '_'))
	{
		str++;
		x++;
	}
	return (x);
}
